const rosterCap = 60

// handleModels returns the analyst model roster: the curated Zen family, then —
// best effort, for a signed-in caller — whatever the user can serve upstream
// (/v1/models) and any agents (/v1/agents). Never 5xx: no bearer or an upstream
// failure simply yields the curated list, so the dropdown always works.
func (s *Server) handleModels(w http.ResponseWriter, r *http.Request) {
	if preflight(w, r, "GET, OPTIONS") {
		return
	}
	if r.Method != http.MethodGet {
		writeError(w, http.StatusMethodNotAllowed, "Method not allowed")
		return
	}

	roster := make([]map[string]string, 0, rosterCap)
	seen := map[string]bool{}
	for _, m := range zenRoster {
		roster = append(roster, m)
		seen[m["id"]] = true
	}

	if bearer := s.ai.bearerFor(r); bearer != "" {
		ctx, cancel := context.WithTimeout(r.Context(), 8*time.Second)
		defer cancel()
		extra := aiForwardHeaders(r)
		for _, id := range s.ai.upstreamModels(ctx, s, bearer, extra) {
			if len(roster) >= rosterCap {
				break
			}
			if id != "" && !seen[id] {
				seen[id] = true
				roster = append(roster, map[string]string{"id": id, "label": id, "group": modelGroup(id)})
			}
		}
		for _, ag := range s.ai.upstreamAgents(ctx, s, bearer, extra) {
			if len(roster) >= rosterCap {
				break
			}
			id := "agent:" + ag.ref
			if ag.ref != "" && !seen[id] {
				seen[id] = true
				label := ag.name
				if label == "" {
					label = ag.ref
				}
				roster = append(roster, map[string]string{"id": id, "label": label, "group": "Agents"})
			}
		}
	}

	writeJSON(w, http.StatusOK, "no-store", map[string]any{"data": roster, "default": s.ai.model})
}

// modelGroup buckets a served model id into its family for the dropdown — the
// server-side mirror of the client's modelFamily (src/utils/model-marks.ts), so
// e.g. gpt-oss never files under Zen.
func modelGroup(id string) string {
	m := strings.ToLower(id)
	switch {
	case m == "best":
		return "Auto"
	case strings.HasPrefix(m, "zen"):
		return "Zen"
	case strings.HasPrefix(m, "gpt"):
		return "GPT"
	case strings.HasPrefix(m, "llama") || strings.Contains(m, "llama"):
		return "Llama"
	case strings.HasPrefix(m, "claude") || strings.HasPrefix(m, "anthropic"):
		return "Claude"
	}
	return "Models"
}

var modelIDRe = regexp.MustCompile(`^[A-Za-z0-9][A-Za-z0-9._:@/-]{0,79}$`)

// sanitizeModel returns a syntactically-valid model id (or "" to use the default).
// A bad id upstream 4xxes into the degrade path, so we only guard the shape.
func sanitizeModel(m string) string {
	m = strings.TrimSpace(m)
	if modelIDRe.MatchString(m) {
		return m
	}
	return ""
}

// agentRef extracts the agent reference from a "agent:<ref>" model id, else "".
func agentRef(model string) string {
	if strings.HasPrefix(model, "agent:") {
		return strings.TrimSpace(strings.TrimPrefix(model, "agent:"))
	}
	return ""
}

// upstreamModels fetches servable model ids from api.hanzo.ai/v1/models (OpenAI
// shape). Any failure (incl. the aud-allowlist 401) yields nil — degrade quietly.
func (a *AIClient) upstreamModels(ctx context.Context, s *Server, bearer string, extra map[string]string) []string {
	headers := map[string]string{"Authorization": bearer}
	for k, v := range extra {
		headers[k] = v
	}
	body, status, err := s.do(ctx, "GET", a.base+"/models", headers, nil)
	if err != nil || status < 200 || status >= 300 {
		return nil
	}
	var parsed struct {
		Data []struct {
			ID string `json:"id"`
		} `json:"data"`
	}
	if json.Unmarshal(body, &parsed) != nil {
		return nil
	}
	ids := make([]string, 0, len(parsed.Data))
	for _, m := range parsed.Data {
		if id := strings.TrimSpace(m.ID); id != "" {
			ids = append(ids, id)
		}
	}
	return ids
}

type agentInfo struct{ ref, name string }

// upstreamAgents fetches the org's agents from api.hanzo.ai/v1/agents. The field
// names vary, so ref/name are read leniently. Any failure yields nil.
func (a *AIClient) upstreamAgents(ctx context.Context, s *Server, bearer string, extra map[string]string) []agentInfo {
	headers := map[string]string{"Authorization": bearer}
	for k, v := range extra {
		headers[k] = v
	}
	body, status, err := s.do(ctx, "GET", a.base+"/agents", headers, nil)
	if err != nil || status < 200 || status >= 300 {
		return nil
	}
	var parsed struct {
		Agents []map[string]any `json:"agents"`
		Data   []map[string]any `json:"data"`
	}
	if json.Unmarshal(body, &parsed) != nil {
		return nil
	}
	rows := parsed.Agents
	if len(rows) == 0 {
		rows = parsed.Data
	}
	out := make([]agentInfo, 0, len(rows))
	for _, m := range rows {
		ref := firstString(m, "ref", "id", "slug", "name")
		if ref == "" {
			continue
		}
		out = append(out, agentInfo{ref: ref, name: firstString(m, "name", "displayName", "title")})
	}
	return out
}

// runAgent invokes an agent in-chat via api.hanzo.ai/v1/agents/<ref>/run and
// returns its text output. Result shapes vary, so text is extracted leniently.
func (a *AIClient) runAgent(ctx context.Context, s *Server, bearer, ref, input string, extra map[string]string) (string, error) {
	reqBody, _ := json.Marshal(map[string]string{"input": input})
	headers := map[string]string{"Authorization": bearer, "Content-Type": "application/json"}
	for k, v := range extra {
		headers[k] = v
	}
	cctx, cancel := context.WithTimeout(ctx, 45*time.Second)
	defer cancel()
	body, status, err := s.do(cctx, "POST", a.base+"/agents/"+url.PathEscape(ref)+"/run", headers, reqBody)
	if err != nil {
		return "", err
	}
	if status < 200 || status >= 300 {
		return "", fmt.Errorf("hanzo agent status %d", status)
	}
	return extractAgentText(body), nil
}

// extractAgentText pulls the assistant text out of a variable agent-run result.
func extractAgentText(b []byte) string {
	var m map[string]any
	if json.Unmarshal(b, &m) != nil {
		return ""
	}
	for _, k := range []string{"output", "result", "reply", "text", "content", "answer", "message"} {
		if v, ok := m[k].(string); ok && strings.TrimSpace(v) != "" {
			return strings.TrimSpace(v)
		}
		if nested, ok := m[k].(map[string]any); ok {
			if t, ok := nested["text"].(string); ok && strings.TrimSpace(t) != "" {
				return strings.TrimSpace(t)
			}
			if t, ok := nested["content"].(string); ok && strings.TrimSpace(t) != "" {
				return strings.TrimSpace(t)
			}
		}
	}
	// OpenAI-ish {choices:[{message:{content}}]}
	if choices, ok := m["choices"].([]any); ok && len(choices) > 0 {
		if c0, ok := choices[0].(map[string]any); ok {
			if msg, ok := c0["message"].(map[string]any); ok {
				if t, ok := msg["content"].(string); ok {
					return strings.TrimSpace(t)
				}
			}
		}
	}
	return ""
}

func firstString(m map[string]any, keys ...string) string {
	for _, k := range keys {
		if v, ok := m[k].(string); ok && strings.TrimSpace(v) != "" {
			return strings.TrimSpace(v)
		}
	}
	return ""
}

// ── Message + question helpers ───────────────────────────────────────────────

// sanitizeAnalystMessages validates roles, drops empties, caps content length,
// and keeps only the most recent analystMaxTurns turns.
func sanitizeAnalystMessages(in []chatMessage) []chatMessage {
	out := make([]chatMessage, 0, len(in))
	for _, m := range in {
		role := strings.ToLower(strings.TrimSpace(m.Role))
		if role != "user" && role != "assistant" {
			continue
		}
		content := strings.TrimSpace(m.Content)
		if content == "" {
			continue
		}
		if len(content) > analystMaxContent {
			content = content[:analystMaxContent]
		}
		out = append(out, chatMessage{Role: role, Content: content})
	}
	if len(out) > analystMaxTurns {
		out = out[len(out)-analystMaxTurns:]
	}
	return out
}

// latestUserContent returns the most recent user turn's text, or "".
func latestUserContent(msgs []chatMessage) string {
	for i := len(msgs) - 1; i >= 0; i-- {
		if msgs[i].Role == "user" {
			return msgs[i].Content
		}
	}
	return ""
}

// timeQuestionCues signal a time-scoped question — the only case worth the digest.
var timeQuestionCues = []string{
	"last ", "past ", "over the", "recent", "trend", "changed", "change",
	"since", "history", "24h", "24 hour", "48h", "hour", "yesterday",
	"today", "this week", "this month", "week", "day", "so far", "escalat",
	"evolv", "developing", "momentum", "trajectory",
}

// isTimeQuestion reports whether q reads as a question about change over time.
func isTimeQuestion(q string) bool {
	q = strings.ToLower(q)
	for _, cue := range timeQuestionCues {
		if strings.Contains(q, cue) {
			return true
		}
	}
	return false
}

// ── Prompt derivation (from the command manifest) ────────────────────────────

// commandTypes is the allow-set of command names the model may emit.
func commandTypes(cmds []analystCommand) map[string]bool {
	m := make(map[string]bool, len(cmds))
	for _, c := range cmds {
		if c.Name != "" {
			m[c.Name] = true
		}
	}
	return m
}

type fieldRef struct {
	name     string
	required bool
}

// orderedFields lists a command's params: required (declared order) then optional
// (sorted) — deterministic, so the rendered contract is stable.
func orderedFields(c analystCommand) []fieldRef {
	reqSet := map[string]bool{}
	out := make([]fieldRef, 0, len(c.Params.Properties))
	for _, r := range c.Params.Required {
		if _, ok := c.Params.Properties[r]; ok {
			out = append(out, fieldRef{r, true})
			reqSet[r] = true
		}
	}
	opt := make([]string, 0, len(c.Params.Properties))
	for k := range c.Params.Properties {
		if !reqSet[k] {
			opt = append(opt, k)
		}
	}
	sort.Strings(opt)
	for _, k := range opt {
		out = append(out, fieldRef{k, false})
	}
	return out
}

// renderField renders one param as it appears in the emitted JSON action, e.g.
// "range":"1h|6h|24h|48h|7d|all"  or  "zoom":<number>?  (? marks optional).
func renderField(name string, p analystProp, required bool) string {
	var val string
	switch {
	case len(p.Enum) > 0:
		parts := make([]string, 0, len(p.Enum))
		for _, e := range p.Enum {
			parts = append(parts, fmt.Sprint(e))
		}
		if p.Type == "string" {
			val = "\"" + strings.Join(parts, "|") + "\""
		} else {
			val = strings.Join(parts, "|")
		}
	case p.Type == "string":
		val = "\"<text>\""
	case p.Type == "number":
		val = "<number>"
	case p.Type == "integer":
		val = "<integer>"
	case p.Type == "boolean":
		val = "true|false"
	default:
		val = "<value>"
	}
	out := "\"" + name + "\":" + val
	if !required {
		out += "?"
	}
	return out
}

// renderCommandContract turns the manifest into the action-contract prompt block.
func renderCommandContract(cmds []analystCommand) string {
	var b strings.Builder
	for _, c := range cmds {
		b.WriteString("- {\"type\":\"" + c.Name + "\"")
		for _, f := range orderedFields(c) {
			b.WriteString("," + renderField(f.name, c.Params.Properties[f.name], f.required))
		}
		b.WriteString("}")
		if d := strings.TrimSpace(c.Description); d != "" {
			b.WriteString("  — " + d)
		}
		b.WriteByte('\n')
	}
	return b.String()
}

// renderToolContract turns the mcp data-tool specs into the tool-contract prompt
// block: one line per tool, `name(required, optional?) — description`. Params are
// listed required-first (declared order) then optional (sorted), mirroring the
// command contract's determinism.
func renderToolContract(specs []mcp.ToolSpec) string {
	var b strings.Builder
	for _, sp := range specs {
		b.WriteString("- " + sp.Name + "(")
		b.WriteString(strings.Join(toolParamList(sp.InputSchema), ", "))
		b.WriteString(")")
		if d := strings.TrimSpace(sp.Description); d != "" {
			b.WriteString(" — " + d)
		}
		b.WriteByte('\n')
	}
	return b.String()
}

// toolParamList renders a tool's params: required (declared order) then optional
// (sorted, marked with a trailing ?). Deterministic regardless of map order.
func toolParamList(schema map[string]any) []string {
	props, _ := schema["properties"].(map[string]any)
	if len(props) == 0 {
		return nil
	}
	reqSet := map[string]bool{}
	out := make([]string, 0, len(props))
	for _, r := range toStringSlice(schema["required"]) {
		if _, ok := props[r]; ok {
			out = append(out, r)
			reqSet[r] = true
		}
	}
	opt := make([]string, 0, len(props))
	for k := range props {
		if !reqSet[k] {
			opt = append(opt, k)
		}
	}
	sort.Strings(opt)
	for _, k := range opt {
		out = append(out, k+"?")
	}
	return out
}

// toStringSlice coerces a JSON "required" value ([]string from Go literals, or
// []any after a round-trip) into []string.
func toStringSlice(v any) []string {
	switch s := v.(type) {
	case []string:
		return s
	case []any:
		out := make([]string, 0, len(s))
		for _, e := range s {
			if str, ok := e.(string); ok {
				out = append(out, str)
			}
		}
		return out
	}
	return nil
}

