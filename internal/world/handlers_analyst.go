// ── AI Analyst (agentic control surface) ─────────────────────────────────────
//
// /v1/world/analyst is a multi-turn chat grounded in a client-composed snapshot
// of the live dashboard. It is BOTH a Q&A analyst and the dashboard's control
// surface: the model may answer in prose AND/OR emit a small vocabulary of typed
// commands that the SPA executor maps 1:1 to app capabilities.
//
// The command vocabulary is NOT duplicated here. The client's command registry
// (src/services/app-commands.ts) is the single source of truth: the SPA sends its
// live manifest with every request, and this handler DERIVES the action contract
// in the system prompt from it (renderCommandContract). Callers that omit a
// manifest fall back to the embedded mirror (data/analyst_commands.json, generated
// from the same registry by scripts/gen-analyst-commands.mjs). The backend never
// re-validates params — it only passes structurally-valid, allow-typed actions
// through; the SPA dispatcher validates every field before it touches the app.
//
// Auth + billing: like every AI endpoint, it forwards the CALLER's IAM bearer to
// Hanzo inference (api.hanzo.ai) so usage meters to their org/project. No shared
// key. (hanzo.chat authenticates identically — forwarding the user's IAM token
// unchanged. World's token carries aud=hanzo-world; that audience must be on the
// gateway/cloud allowlist for the forward to be accepted — see the transport report.)

const analystMaxTurns = 12      // history cap (cost + context guard)
const analystMaxContent = 8000  // per-message content cap (abuse / OOM guard)
const analystContextCap = 12000 // client grounding-snapshot cap

// Agentic data-tool loop bounds. The analyst may call the world MCP data tools
// (mcp.ToolSpecs) up to analystMaxToolRounds times before it MUST answer, so a
// reply cites live server-side data. Bounded on every axis (rounds, calls/round,
// injected-result size) to cap cost + context.
const analystMaxToolRounds = 3      // max tool-executing rounds before a forced answer
const analystMaxToolsPerRound = 4   // max tool calls honoured in a single round
const analystToolResultCap = 4000   // per-result chars fed back to the model
const analystTraceResultCap = 1200  // per-result chars surfaced to the chat UI

// defaultCommandsJSON mirrors the client registry for callers that don't send a
// manifest. Generated by scripts/gen-analyst-commands.mjs; verified by test.
//
//go:embed data/analyst_commands.json
var defaultCommandsJSON []byte

// analystCommand is one entry of the command manifest (client-sent or embedded).
type analystCommand struct {
	Name        string        `json:"name"`
	Description string        `json:"description"`
	Params      analystParams `json:"params"`
	Authed      bool          `json:"authed"`
}

type analystParams struct {
	Type       string                 `json:"type"`
	Properties map[string]analystProp `json:"properties"`
	Required   []string               `json:"required"`
}

type analystProp struct {
	Type        string `json:"type"`
	Enum        []any  `json:"enum,omitempty"`
	Description string `json:"description,omitempty"`
}

// defaultCommands decodes the embedded fallback manifest.
func defaultCommands() []analystCommand {
	var cmds []analystCommand
	_ = json.Unmarshal(defaultCommandsJSON, &cmds)
	return cmds
}

// sanitizeCommands keeps well-formed, uniquely-named commands, preserving order.
func sanitizeCommands(in []analystCommand) []analystCommand {
	out := make([]analystCommand, 0, len(in))
	seen := map[string]bool{}
	for _, c := range in {
		name := strings.TrimSpace(c.Name)
		if name == "" || seen[name] || c.Params.Type != "object" {
			continue
		}
		seen[name] = true
		c.Name = name
		out = append(out, c)
	}
	return out
}

// handleAnalyst answers a multi-turn analyst conversation and, when the user asks
// for a change, returns typed commands for the SPA to execute.
func (s *Server) handleAnalyst(w http.ResponseWriter, r *http.Request) {
	if preflight(w, r, "POST, OPTIONS") {
		return
	}
	if r.Method != http.MethodPost {
		writeError(w, http.StatusMethodNotAllowed, "Method not allowed")
		return
	}
	var body struct {
		Messages []chatMessage    `json:"messages"`
		Context  string           `json:"context"`
		Commands []analystCommand `json:"commands"`
		Model    string           `json:"model"`
	}
	if err := decodeJSONBody(r, &body); err != nil {
		writeError(w, http.StatusBadRequest, "Invalid JSON body")
		return
	}
	msgs := sanitizeAnalystMessages(body.Messages)
	if len(msgs) == 0 {
		writeError(w, http.StatusBadRequest, "messages array required")
		return
	}

	// Paid usage: the analyst chat is metered to the signed-in user's org, so it
	// requires the caller's OWN IAM bearer — never the funded service key. That key
	// (a.key / HANZO_AI_KEY) backs ONLY the anonymous auto-insight endpoints
	// (summarize/classify/country-intel via bearerFor); chat must not silently burn
	// it, or "paid usage" would never actually meter. Signed-out callers get a quiet
	// sign-in prompt (never a 5xx).
	bearer := userBearer(r)
	if bearer == "" {
		writeJSON(w, http.StatusOK, "", map[string]any{
			"reply": "", "actions": []any{}, "fallback": true, "skipped": true,
			"reason": "Sign in to chat with the analyst",
		})
		return
	}

	// Command contract: client manifest is the source of truth; embedded mirror
	// is the fallback. The allowed-type set gates which actions pass through.
	cmds := sanitizeCommands(body.Commands)
	if len(cmds) == 0 {
		cmds = defaultCommands()
	}
	allowed := commandTypes(cmds)

	// Two contracts frame the model: the client-derived app-COMMAND contract (UI
	// control) and the world data-TOOL contract (live reads, executed in-process).
	// Both derive from a single source of truth — the SPA command manifest and the
	// mcp tool registry — never duplicated here.
	toolSpecs := mcp.ToolSpecs()
	system := analystSystemPrompt(body.Context, renderCommandContract(cmds))
	system += "\n\n" + analystToolsBlock(renderToolContract(toolSpecs))
	// Time-scoped questions get grounded in the durable model history digest.
	if isTimeQuestion(latestUserContent(msgs)) {
		if digest := s.worldModel.HistoryDigest(24); digest != "" {
			system += "\n\n" + digest
		}
	}

	ctx, cancel := context.WithTimeout(r.Context(), 45*time.Second)
	defer cancel()

	model := sanitizeModel(body.Model)
	extra := aiForwardHeaders(r)

	// Agent path: a "agent:<ref>" model routes to the agents plane (invoke an
	// agent in-chat) instead of chat/completions. Prose result, same contract.
	if ref := agentRef(model); ref != "" {
		out, err := s.ai.runAgent(ctx, s, bearer, ref, latestUserContent(msgs), extra)
		if err != nil || out == "" {
			writeJSON(w, http.StatusOK, "", map[string]any{
				"reply": "", "actions": []any{}, "fallback": true, "model": model, "error": errStr(err),
			})
			return
		}
		reply, actions := parseAnalystOutput(out, allowed)
		writeJSON(w, http.StatusOK, "", map[string]any{"reply": reply, "actions": actions, "model": model})
		return
	}

	toolNames := toolNameSet(toolSpecs)

	full := make([]chatMessage, 0, len(msgs)+1+analystMaxToolRounds*2)
	full = append(full, chatMessage{Role: "system", Content: system})
	full = append(full, msgs...)

	// Agentic loop: each turn the model may (a) request data tools, which we run
	// in-process and feed back, or (b) answer. It runs at most analystMaxToolRounds
	// tool rounds before a final answer is forced. Tool traces accumulate for the
	// chat UI. When the model needs no data, this collapses to a single call —
	// byte-for-byte the prior behaviour.
	var (
		reply   string
		actions []map[string]any
		traces  = make([]map[string]any, 0, analystMaxToolRounds)
		tokens  int
	)
	for round := 0; ; round++ {
		out, tk, err := s.ai.chatMessagesModel(ctx, s, bearer, model, full, 0.4, 700, extra)
		if err != nil || out == "" {
			// Surface the upstream reason honestly — the SPA renders it instead of a
			// blank chat (e.g. a 401 when aud=hanzo-world isn't allow-listed upstream).
			// Any tool traces gathered so far still ride along so partial work shows.
			writeJSON(w, http.StatusOK, "", map[string]any{
				"reply": "", "actions": []any{}, "fallback": true, "error": errStr(err), "traces": traces,
			})
			return
		}
		tokens += tk
		r, acts, calls := parseAnalystTurn(out, allowed, toolNames)
		if len(calls) == 0 || round >= analystMaxToolRounds {
			reply, actions = r, acts
			break
		}
		// Execute the requested tools in-process and feed the results back as the
		// next turn's grounding. The model's raw request rides as the assistant turn
		// so the transcript stays coherent.
		full = append(full, chatMessage{Role: "assistant", Content: out})
		last := round == analystMaxToolRounds-1
		full = append(full, chatMessage{Role: "user", Content: s.runAnalystTools(ctx, calls, &traces, last)})
	}

	served := model
	if served == "" {
		served = s.ai.model
	}
	writeJSON(w, http.StatusOK, "", map[string]any{
		"reply": reply, "actions": actions, "model": served, "tokens": tokens, "traces": traces,
	})
}

// runAnalystTools executes a round's tool calls IN-PROCESS through the mcp
// dispatcher (one way — the same path an external MCP client uses), appends a
// trace per call for the chat UI, and returns the tool-results block injected back
// into the conversation as the next user turn. Results are capped on both the
// model-facing (analystToolResultCap) and UI-facing (analystTraceResultCap) sides.
func (s *Server) runAnalystTools(ctx context.Context, calls []analystToolCall, traces *[]map[string]any, lastRound bool) string {
	var b strings.Builder
	b.WriteString("TOOL RESULTS (live data — ground your answer in these; do not repeat the same call):\n")
	for _, c := range calls {
		argJSON, _ := json.Marshal(c.Args)
		if c.Args == nil {
			argJSON = []byte("{}")
		}
		body, ok := s.mcp.CallTool(ctx, c.Name, c.Args)
		label := c.Name + "(" + string(argJSON) + ")"
		b.WriteString("🔧 " + label + " -> ")
		if !ok {
			b.WriteString("ERROR: ")
		}
		b.WriteString(capString(body, analystToolResultCap))
		b.WriteString("\n\n")
		*traces = append(*traces, map[string]any{
			"label":  label,
			"ok":     ok,
			"result": capString(body, analystTraceResultCap),
		})
	}
	if lastRound {
		b.WriteString("This was the final tool round. Answer the user now in prose using the data above; do not request more tools.\n")
	}
	return b.String()
}

// capString trims s to n runes, appending an ellipsis marker when truncated. Byte
// slicing is safe here (tool bodies are JSON/ASCII-dominant) but we cut on a rune
// boundary to avoid emitting invalid UTF-8.
func capString(s string, n int) string {
	s = strings.TrimSpace(s)
	if len(s) <= n {
		return s
	}
	cut := n
	for cut > 0 && !utf8ValidCut(s, cut) {
		cut--
	}
	return s[:cut] + "…(truncated)"
}

func utf8ValidCut(s string, i int) bool { return i <= 0 || i >= len(s) || (s[i]&0xC0) != 0x80 }

// toolNameSet is the allow-set of data-tool names the model may call.
func toolNameSet(specs []mcp.ToolSpec) map[string]bool {
	m := make(map[string]bool, len(specs))
	for _, sp := range specs {
		m[sp.Name] = true
	}
	return m
}

