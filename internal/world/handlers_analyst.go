// analystToolCall is one validated data-tool request emitted by the model.
type analystToolCall struct {
	Name string
	Args map[string]any
}

// parseAnalystOutput extracts {reply, actions} from the model output — the
// non-agentic path (agent runs, and the thin wrapper for callers that don't run
// the tool loop). It is parseAnalystTurn with tool calls ignored, so there is one
// parse implementation.
func parseAnalystOutput(raw string, allowed map[string]bool) (string, []map[string]any) {
	reply, actions, _ := parseAnalystTurn(raw, allowed, nil)
	return reply, actions
}

// parseAnalystTurn extracts {reply, actions, tools} from one model turn. Actions
// pass through as generic objects (the SPA validates every field); only actions
// whose "type" is in the allow-set, and only tool calls whose "name" is a known
// data tool, survive. If nothing parses, the whole output is the reply with no
// actions/tools (safe degrade — a chat answer is never lost). A nil toolNames
// disables tool extraction entirely.
func parseAnalystTurn(raw string, allowed, toolNames map[string]bool) (string, []map[string]any, []analystToolCall) {
	obj := extractJSONObject(raw)
	if obj == "" {
		return strings.TrimSpace(raw), nil, nil
	}
	var parsed struct {
		Reply   string           `json:"reply"`
		Actions []map[string]any `json:"actions"`
		Tools   []struct {
			Name      string         `json:"name"`
			Arguments map[string]any `json:"arguments"`
		} `json:"tools"`
	}
	if err := json.Unmarshal([]byte(obj), &parsed); err != nil {
		return strings.TrimSpace(raw), nil, nil
	}
	actions := make([]map[string]any, 0, len(parsed.Actions))
	for _, a := range parsed.Actions {
		t, _ := a["type"].(string)
		t = strings.ToLower(strings.TrimSpace(t))
		if t == "" || !allowed[t] {
			continue
		}
		a["type"] = t
		actions = append(actions, a)
	}
	var calls []analystToolCall
	if toolNames != nil {
		for _, tc := range parsed.Tools {
			name := strings.TrimSpace(tc.Name)
			if name == "" || !toolNames[name] {
				continue
			}
			if len(calls) >= analystMaxToolsPerRound {
				break
			}
			calls = append(calls, analystToolCall{Name: name, Args: tc.Arguments})
		}
	}
	reply := strings.TrimSpace(parsed.Reply)
	if reply == "" && len(actions) == 0 && len(calls) == 0 {
		return strings.TrimSpace(raw), nil, nil
	}
	return reply, actions, calls
}

// extractJSONObject returns the outermost {...} span in s (fence/prose tolerant),
// or "" if none is balanced.
func extractJSONObject(s string) string {
	start := strings.Index(s, "{")
	if start < 0 {
		return ""
	}
	depth, inStr, esc := 0, false, false
	for i := start; i < len(s); i++ {
		c := s[i]
		switch {
		case esc:
			esc = false
		case c == '\\' && inStr:
			esc = true
		case c == '"':
			inStr = !inStr
		case inStr:
			// skip
		case c == '{':
			depth++
		case c == '}':
			depth--
			if depth == 0 {
				return s[start : i+1]
			}
		}
	}
	return ""
}
